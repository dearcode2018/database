/* 分页查询 */

/* 分页-查询 示例 
使用rownum方式 
(指定记录的起止下标) */
/* 注意 : 包含起止下标  [start_index, end_index] */
SELECT t.* FROM 
(SELECT a.*, rownum AS rNum FROM tb_name a WHERE  rownum <= end_index) t
WHERE t.rNum >= start_index;

