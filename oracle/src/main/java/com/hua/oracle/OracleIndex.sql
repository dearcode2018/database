/* 创建索引 */
CREATE [UNIQUE | BITMAP | B-TREE(全部省略时使用该值)] INDEX index_name ON tb_name
column_name | expression ASC | DESC,...
TABLESPACE tb_space_name 
STORAGE storage_settings
/* 是否记日志 */
LOGGING | NOLOGGING
/* 创建索引时收集统计信息 */
COMPUTE STATISTICS
NOCOMPRESS | COMPRESS nn
NOSORT | REVERSE
PARTITION | GLOBAL PARTITION;

