/* 创建函数 */
create or replace function fun_name (param IN | OUT | IN OUT dataType default value,...) is
	/* 变量声明 (不能加 declare) */
	variableName dataType([length]) default value;
	variableName dataType([length]) := value;
	/* 定义返回值_类型 */
	return returnType;
begin
	/* 若没有语句，写 null; */
	plsql statement;
	-- return ret_value;
end;


