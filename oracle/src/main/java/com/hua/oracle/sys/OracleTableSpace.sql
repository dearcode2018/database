/* ====================  ==================== */

/* 创建临时表空间 */
CREATE TEMPORARY TABLESPACE tbs_name
LOGGING
TEMPFILE 'dir.../xx.dbf'
SIZE xxM
AUTOEXTEND ON
NEXT xxM
MAXSIZE xxM
EXTENT MANAGEMENT LOCAL;



/* ====================  ==================== */

/* 创建表空间 */
CREATE TABLESPACE table_space_name
DATAFILE 'dir../xx.dbf'
SIZE xxM
AUTOEXTEND ON
NEXT
MAXSIZE
EXTENT MANAGEMENT LOCAL;

/* ====================  ==================== */

/* 删除该表空间在Db内的文件信息，物理文件并不删除 */
DROP TABLESPACE table_space_name; 

/* 删除内容和物理文件 */
DROP TABLESPACE table_space_name INCLUDING CONTENTS AND DATAFILES; 

/* ====================  ==================== */
/* 修改表空间 */	
ALTER TABLESPACE table_space_name;

