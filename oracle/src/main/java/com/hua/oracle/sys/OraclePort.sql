/* 这些控制台命令，尽量不要在最后加分号结束，分号会引发命令解析的异常 */
/* 使用有dba权限（system）用户登录 */
/* 导出数据库所有用户的所有数据 */
xx > exp userid=system/password@db_instance file=file_want_to_save full=y;

/* 导出1/多个用户的所有数据 */
xx > exp system/password@db_instance file=file_want_to_save owner=(user1,user2,...);

/* 导出一个用户的所有数据 */
xx > exp username/password@db_instance file=file_want_to_save full=y;

/* 导出一个用户的部分数据（1/多张表） */
xx > exp username/password@db_instance file=file_want_to_save tables=(tb1,tb2,...);

/* 导出一个用户单个表中符合条件的数据 */
