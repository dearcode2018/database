/* 创建视图 */
CREATE OR REPLACE [FORCE | NOFORCE] VIEW view_name[(列的别名,...)] AS
(select语句) [WITH CHECK OPTION CONSTRAINT constraint_name constraint_def
WITH READ ONLY];

/* 删除视图 */
DROP VIEW view_name;

/* 修改视图 */
--通过 OR REPLACE 来重新创建视图。

/* 查询视图 */
SELECT * FROM view_name;

