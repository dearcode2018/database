/* 分页查询 */

/*
	sql server 分页查询 使用关键字 TOP
*/

SELECT TOP number | percent DISTINCT view_field FROM tb_list 
WHERE filter_condtion_list 
GROUP BY (case_clause or group_field) HAVING group_condition
ORDER BY field1 ASC, field2 DESC;


